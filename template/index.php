                            <p>
                                Die nächste Generation Jazz Musiker mischt das Feld auf—und wie. Gerade mal etwas über 20
                                Jahre alt spielen die vier Protagonisten des District Five Quartets schon ganz oben mit.
                                Sie kennen sich sehr gut und seit langem und erproben das Zusammenspiel in jeder freien
                                Minute. Die Band nimmt die Strömungen des heutigen Jazz auf und formt daraus eine eigene
                                Klangsprache mit Perspektive auf Tradition. Gleichzeitig beweist sie aber auch Mut Grenzen
                                zu verschieben. Eine Musik voller Ideen und Spannungsbögen mit ungeheurer Energie und
                                Selbstbewusstsein—immer auf Draht, heraus aus dem Moment und hinein ins Geschehen.
                                Zeitgenössisch—virtuoser Jazz mit amerikanischem Flair! <span class="author">(Text:
                                    Christoph Irniger)</span>
                            </p>
